import math

val = 10.1
sqrt_val = math.sqrt(val)
print(sqrt_val**2) #sqrtval^2


exp_val = math.exp(val)
print(exp_val) #e^(expval)

fact_val = math.factorial(math.floor(val)) #31 = 3*2*1
print(fact_val)

sin_val = math.sin(val)
print(sin_val)

floor_val = math.floor(val) #把數字變小
print(floor_val)

ceiling_val = math.ceil(val) #把數字變大
print(ceiling_val)